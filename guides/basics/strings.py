# William is a string defined by quotes around any value.
"William"

print("William")
# Expected Output:
# William

# You can assign a string to a variable.
name = "William"
print(name)
# Expected Output:
# William

# You can combine strings
first_name = "William"
last_name = "Wallace"

full_name = first_name + " " + last_name
print(full_name)
# Expected Output:
# William Shakespeare

# You can use a f-string to format strings
print(f"Hello, {full_name}")